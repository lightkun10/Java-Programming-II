package Lectures.L2_Sorting_Method_as_Lambda_Expression;

public class Person {
    // Instance Variable
    private int birthYear;
    private String name;

    // Constructor
    public Person(String name, int birthYear) {
        this.birthYear = birthYear;
        this.name = name;
    }

    // Methods
    public String getName() {
        return name;
    }

    public int getBirthYear() {
        return birthYear;
    }
}